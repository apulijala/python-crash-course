
def alien_color_if(color):
    if color.lower() == "green":
        return 5
    else: 
        return 10

def alien_color_if_else_if(color):
    if color.lower() == "green":
        return 5
    elif color.lower() == "yellow": 
        return 10
    elif color.lower() == "red": 
        return 15
    else:
        return 20

